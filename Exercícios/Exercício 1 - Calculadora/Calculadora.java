public class Calculadora {
    public static void Soma(float num1, float num2) {
         float soma = num1 + num2;
        System.out.printf("Seu resultado é: " + soma);

    }

    public static void Subtracao(float num1, float num2) {
        float sub = num1 - num2;
        System.out.printf("Seu resultado é: " + sub);

    }

    public static void Mult(float num1, float num2) {
        float mult = num1 * num2;
        System.out.printf("Seu resultado é: " + mult);

    }

    public static void Div(float num1, float num2) {
        float div = num1/num2;
        System.out.printf("Seu resultado é: " + div);

    }





}
